export function Providers({ children }: { children: React.ReactNode }) {
  const [queryClient] = useState(
    () =>
      new QueryClient({
        defaultOptions: {
          queries: {
            staleTime: 60 * 1000,
            refetchOnWindowFocus: false,
            retry: 1,
            refetchOnMount: true,
            refetchOnReconnect: true,
          },
        },
      })
  );

  return (
    <QueryClientProvider client={queryClient}>
      <ThirdwebProvider
        activeChain={Sepolia}
        clientId={process.env.NEXT_PUBLIC_THIRDWEB_CLIENT_ID}
        queryClient={queryClient}
        supportedChains={[Sepolia]}
        dAppMeta={{
          name: "BlockFund",
          description: "A decentralized crowdfunding platform",
          logoUrl: "/thirdweb.png",
          url: "https://blockfund.com",
        }}
        storageInterface={{
          upload: async (data) => {
            const formData = new FormData()
            formData.append('file', data[0])
            
            const response = await fetch('https://api.thirdweb.com/storage/upload', {
              method: 'POST',
              body: formData,
              headers: {
                'Authorization': `Bearer ${process.env.NEXT_PUBLIC_THIRDWEB_CLIENT_ID}`
              }
            })
            
            const result = await response.json()
            return result.url
          }
        }}
      >
        {children}
        <ReactQueryDevtools initialIsOpen={false} />
      </ThirdwebProvider>
    </QueryClientProvider>
  );
} 